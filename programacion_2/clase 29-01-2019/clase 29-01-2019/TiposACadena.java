public class TiposACadena{

    public static void main(String [] args){
    
        int k= -12;
        char c = 'F';
        long k1 = 9999999L;
        boolean sw = false;
        float f = 25.50f;
        double d = -15.45;
        
        System.out.println("Entero k:" + String.valueOf(k));
        System.out.println("Caracter c:" + String.valueOf(c));
        System.out.println("Long k1:" + String.valueOf(k1));
        System.out.println("Boolean sw:" + String.valueOf(sw));
        System.out.println("Float f:" + String.valueOf(f));
        System.out.println("Double d:" + String.valueOf(d));
        
        
    }
}